"""
Calculator functions
"""

def add(x, y):
    """
    Add x and y and return result
    :param x:
    :param y:
    :return:
    """
    return x + y

def subtract(x, y):
    """
    Subtract x from y and return result.
    :param x:
    :param y:
    :return:
    """
    return y - x

def even_or_odd(x, y):
    """
    Even or Odd number
    :param x:
    :param y:
    :return:
    """
    if x % 2 == y:
        return True
    return False